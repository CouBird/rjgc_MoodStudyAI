pub fn generate_template_feedback(emotion_tag: &str) -> String {
    match emotion_tag {
        "平静" => "今天的节奏很稳定，继续保持这份专注。".to_string(),
        "自豪" => "这份成就感值得被记住，你已经完成了重要的一步。".to_string(),
        "满足" => "能感到满足说明你认真投入了当下的学习。".to_string(),
        "快乐" => "带着这份轻松的状态继续前进，学习也可以是愉快的。".to_string(),
        "疲惫" => "疲惫说明你已经投入了不少精力，可以给自己一个短暂休息。".to_string(),
        "焦虑" => "先把任务拆小，慢慢完成一件事，你不需要一次解决全部问题。".to_string(),
        "难过" => "低落的时候也可以慢一点，今天的坚持仍然有价值。".to_string(),
        _ => "每一种情绪都值得被接纳，感谢你今天继续学习。".to_string(),
    }
}
